public class CharEx {
	public static void main(String[] args) {
		char ch = 'A';
		System.out.println(ch);
		ch = 97;
		System.out.println(ch);
		ch = 48;
		System.out.println(ch);
		ch = 49;
		System.out.println(ch);
		ch = '강';
		System.out.println(ch+0);
		ch = '강';
		System.out.println(ch+1);
		System.out.println((char)(ch+1));
		ch = '힇';
		System.out.println(ch+0);
		
		
		
	}
}
